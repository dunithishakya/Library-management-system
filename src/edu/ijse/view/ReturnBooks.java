    private void searchbook() {
        try {
            String Bid = tblreturnbook.getValueAt(tblreturnbook.getSelectedRow(), 0).toString();
            ReturnbookDto dto = returnBookController.get(Bid);

            if (dto != null) {
                txtbid.setText(dto.getBid());
                txtmid.setText(dto.getMid());
                txtrdate.setText(dto.getRdate());
                txtidate.setText(dto.getIdate());
                txtfines.setText(dto.getFines());
            } else {
                JOptionPane.showMessageDialog(this, "book Not Found");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at loading book");
        }
    }
    
    private void deletebook(){
        try {
            String Bid = txtbid.getText();
            String resp = returnBookController.delete(Bid);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at Delete book");
        }
    }

    private void updatebook() {
        try {
            ReturnbookDto returnbookDto = new ReturnbookDto(txtbid.getText() , txtmid.getText(),txtrdate.getText(), txtidate.getText(), txtfines.getText());
            String resp = returnBookController.update(returnbookDto);
            JOptionPane.showMessageDialog(this, resp);
            loadTable();
            clearForm();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error at update book");
        }
    }
