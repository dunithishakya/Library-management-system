private void addbook(){
        try {
            ReturnbookDto dto = new ReturnbookDto(txtbid.getText(),  txtmid.getText(),txtrdate.getText(),txtidate.getText(),txtfines.getText());
            String resp = returnBookController.add(dto);
            JOptionPane.showMessageDialog(this, resp);
            clearForm();
            loadTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error at save data");
        }

    }

    
    private void loadTable() {
         try {
            String columns[] = {" Bid",  "Mid","Rdate", "Idate"};
            DefaultTableModel dtm = new DefaultTableModel(columns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            tblreturnbook.setModel(dtm);

            ArrayList<ReturnbookDto> returnbookDtos = returnBookController.getAll();
            for (ReturnbookDto dto : returnbookDtos) {
                Object[] rowData = {dto.getBid(),  dto.getMid(),dto.getRdate(),dto.getIdate(),dto.getFines()};
                dtm.addRow(rowData);
            }
        } catch (Exception e) {
        e.printStackTrace();
        }
    }
    
    private void clearForm() {
        txtbid.setText("");
        txtmid.setText("");
        txtrdate.setText("");
        txtidate.setText("");
        txtfines.setText("");
    }

